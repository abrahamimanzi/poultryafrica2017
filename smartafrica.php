                                <p style="line-height: 1.6">The Transform Africa Summit held in Kigali, Rwanda on 28th-31st October 2013 culminated in the adoption of the Smart Africa Manifesto document by 7 African Heads of States in which they committed to provide leadership in accelerating socio-economic development through ICT.<br>
                                On 30th-31st January 2014, The SMART Africa Manifesto was endorsed by all heads of State and Government of the African Union at the 22nd Ordinary Session Assembly of the African Union in Addis Ababa.<br><br>
                                This development places the Manifesto at the heart of the ICT agenda in Africa the 7 original signatories at the Summit to all the 54 African countries.
                                <hr class="clear">
                                <h2 class="text-left"><strong>SMART AFRICA</strong><span class="light"> ALLIANCE</span>
                                </h2>
                                <hr class="hr-book-layout"/>
                                <p style="line-height: 1.6">
                                 The Alliance is a framework for implementation, monitoring and evaluation of the SMART Africa Manifesto, designed to make it actionable. Currently, the Alliance is a partnership bringing together all African countries adhering to the Manifesto, the Private sector as well regional and international organizations such as the <strong>African Union</strong> (AU, the <strong>International Telecommunications Union</strong> (ITU), <strong>World Bank</strong>, <strong>African Development Bank</strong> (AfDB), <strong>UN Economic Commission for Africa</strong> (UNECA), <strong>African Telecommunications Union</strong> (ATU), <strong>New Partnership for Africa’s Development</strong> (NEPAD), the <strong>GSM Association</strong> (GSMA), ICANN<br><br>
                                  Besides its initial membership, other organizations and countries sharing the same vision, interests and goals are admitted to the Alliance.<br><br>
                                  SMART Africa has 5 pillars which reflect the 5 principles of the Smart Africa Manifesto: Policy, Access, e-Government, Private Sector/Entrepreneurship and Sustainable Development. Each pillar is built on four, cross-cutting enablers which will support the implementation of SMART Africa: Innovation; Communications and Advocacy; Capacity Building and Resource Mobilization.<br><br>
                                  The 5 pillars and 4 enablers when effectively developed and combined will contribute to Economic Growth and Job creation, which remains the ultimate goal of the Smart Africa Manifesto.<br><br>
